package codeforces;
import java.util.*;

public class El_fucho {
    public static void main(String[] args) {
        Scanner nm = new Scanner(System.in);
        int test = nm.nextInt(); 
        
        while (test-- > 0) {
            int team = nm.nextInt(); 
            System.out.println(2 * team - 2); 
        }
    }
}

